            <h3><a href="#">GST Registration</a></h3>
            <p>Every business carrying out a taxable supply of goods or services under GST regime and whose turnover exceeds the threshold limit of Rs. 20 lakh/ 10 Lakh as applicable will be required to register as a normal taxable person. This process
              is of registration is referred as GST registration.<br> GST registration is critical because it will enable you to avail various benefits that are available under the GST regime. One such benefit is to avail seamless input tax credit.
              Multiple taxes are being clubbed under GST and thus the cascading of taxes that is prevailing currently will no longer be the case.<br> Also, timely registration will help you avoid any kind of interface with tax authorities.<br> Following
              are the persons required to take registration under this act <br> (a) Persons making inter-state supply, irrespective of any threshold limit <br> (b) Casual taxable persons, irrespective of the threshold specified <br> (c) Persons who
              are required to pay tax under reverse charge <br> (d) Non resident taxable persons <br> (e) Persons who are required to deduct tax under section 37 (TDS)<br> (f) Agents <br> (g) Input service distributor <br> (h) Supply of goods or services
              through electronic commerce operator, other than branded services <br> (i) Every electronic commerce operator <br> (j) Aggregator who supplies service under his brand name or his trade name <br> (k) Other notified persons <br> In the situation
              other than above– <br> (l) Suppliers liable to be registered where it makes a taxable supply of goods of services if its aggregate turnover in a financial year exceeds ₹ 9 lacs. (m) Supplier liable to be registered where it makes a taxable
              supply of goods of services if its aggregate turnover in a financial year exceeds ₹ 4 lacs. (for NE states including Sikkim) (n) Persons who were already registered under the earlier law subject to point (l) and (m) above. (o) Transferee
              in case where business is transferred.</p><a href="#" class="read-more">Read More</a>
