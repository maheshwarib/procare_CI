            <h3><a href="#">About GST</a></h3>
            <p>GST is one indirect tax for the whole nation, which will make India one unified common market. GST is a single tax on the supply of goods and services, right from the manufacturer to the consumer. Credits of input taxes paid at each stage
              will be available in the subsequent stage of value addition, which makes GST essentially a tax only on value addition at each stage. The final consumer will thus bear only the GST charged by the last dealer in the supply chain, with set-off
              benefits at all the previous stages.</p>

