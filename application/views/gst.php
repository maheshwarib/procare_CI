            <h3><a href="#">GST Migration/GST Enrollment</a></h3>
            <p>All existing Central Excise and Service Tax assessees will be migrated to GST starting 7th January 2017. To migrate to GST, assessees would be provided a Provisional ID and Password by CBEC. Provisional IDs would be issued to only those
              assessees who have a valid PAN associated with their registration. An assessee may not be provided a Provisional ID in the following cases:<br> a.The PAN associated with the registration is not valid<br> b.The PAN is registered with State
              a Tax authority and Provisional ID has been supplied by the said State Tax authority.<br> c.There are multiple CE/ST registrations on the same PAN in a State. In this case only 1 Provisional ID would be issued for the 1st registration
              in the alphatebical order provided any of the above 2 conditions are not met.<br>
              <br> The assessees need to use this Provisional ID and Password to logon to the GST Common Portal(https://www.gst.gov.in) where they would be required to fill and submit the Form 20 along with necessary supporting documents.</p>
